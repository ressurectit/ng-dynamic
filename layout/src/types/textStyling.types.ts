/**
 * Available types of font weight
 */
export enum TextFontWeight
{
    /**
     * Font weight light
     */
    Light = 200,

    /**
     * Font weight normal
     */
    Normal = 400,

    /**
     * Font weight semi-bold
     */
    SemiBold = 600,

    /**
     * Font weight bold
     */
    Bold = 700,
}

/**
 * Available types of font size units
 */
export enum StyleSizeUnit
{
    /**
     * Style size unit is in pixels
     */
    px,

    /**
     * Style size unit is in points
     */
    pt,
    
    /**
     * Style size unit is in em
     */
    em,
}